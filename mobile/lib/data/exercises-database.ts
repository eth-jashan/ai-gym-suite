// ============================================================================
// UTILITY FUNCTIONS
// ============================================================================

export function getExerciseById(id: string): DetailedExercise | undefined {
  return EXERCISES_DATABASE.find((ex) => ex.id === id);
}

export function getExerciseBySlug(slug: string): DetailedExercise | undefined {
  return EXERCISES_DATABASE.find((ex) => ex.slug === slug);
}

export function getExercisesByCategory(category: string): DetailedExercise[] {
  return EXERCISES_DATABASE.filter((ex) => ex.category === category);
}

export function getExercisesByMuscle(muscle: string): DetailedExercise[] {
  return EXERCISES_DATABASE.filter(
    (ex) =>
      ex.primaryMuscles.some((m) => m.toLowerCase().includes(muscle.toLowerCase())) ||
      ex.secondaryMuscles.some((m) => m.toLowerCase().includes(muscle.toLowerCase()))
  );
}

export function getExercisesByEquipment(equipment: string[]): DetailedExercise[] {
  if (equipment.length === 0) {
    return EXERCISES_DATABASE.filter((ex) => ex.equipmentRequired.length === 0);
  }
  return EXERCISES_DATABASE.filter((ex) =>
    ex.equipmentRequired.every((eq) =>
      equipment.some((e) => e.toLowerCase().includes(eq.toLowerCase()))
    )
  );
}

export function getExercisesByDifficulty(maxDifficulty: number): DetailedExercise[] {
  return EXERCISES_DATABASE.filter((ex) => ex.difficultyLevel <= maxDifficulty);
}
